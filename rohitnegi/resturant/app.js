document.getElementById('applyFilter').addEventListener('click' , () =>{
    const ele = document.querySelector('input[name="filterOption"]:checked');
    const ans = ele.value;

    if(ans == "rating"){
        restaurant.sort((a,b) => b.rating - a.rating);
    }
    else if (ans== "highlow"){
        restaurant.sort((a,b)=> b.price_for_two - a.price_for_two);
    }
    else if(ans == "costLowHigh"){
        restaurant.sort((a,b)=>a.price_for_two-b.price_for_two);
    }

    else if(ans == "distance"){
        restaurant.sort((a,b)=>parseInt(a.distance)-parseInt(b.distance));
    }

    // const result = restaurant.filter((obj)=>obj.alcohol);
    document.getElementById('root').innerHTML="";
    document.getElementById("filterPopup").classList.add("hidden")
    getresturant(restaurant);
})